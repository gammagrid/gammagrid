import os

DB_PATH = os.environ.get("OPTIONS_TRACKER_DB", "data/options.db")

# Unusual activity (ТЗ, FR16): объём должен быть z-score-выбросом относительно
# собственной истории контракта, а не превышать плоский множитель — иначе на
# ликвидных тикерах флагуются тысячи строк без сигнала.
UNUSUAL_Z_THRESHOLD = 2.5
# Отсекает шум по неликвидным дальним страйкам, где даже большой относительный
# скачок объёма ничего не значит в деньгах.
UNUSUAL_MIN_VOLUME = 50
# Меньше стольки снэпшотов истории по контракту — z-score недостоверен,
# используется упрощённый fallback (volume > 2×OI).
UNUSUAL_MIN_HISTORY_POINTS = 5

# Безрисковая ставка для формулы Блэка-Шоулза в расчёте греков и GEX (ТЗ, FR6, FR14).
# Фиксированная константа, а не рыночная ставка — точность здесь не критична,
# греки слабо чувствительны к небольшим изменениям r.
RISK_FREE_RATE = 0.05

MAX_FETCH_RETRIES = 3
BACKOFF_BASE_SECONDS = 2

# Порог доли контрактов с open_interest=0 в свежесобранной цепочке, выше
# которого снэпшот считается подозрительным и не сохраняется. Найдено на
# реальном инциденте (2026-07-17): источник данных дважды подряд вернул
# цепочку с рабочими volume/ценами, но open_interest=0 почти везде (94.4%
# у SPY против обычных ~8%) и заниженным IV — снэпшот выглядел "успешным",
# но ломал GEX Heatmap/OI Delta. 0.5 — большой запас над нормальным уровнем.
MAX_ZERO_OI_FRACTION = 0.5
